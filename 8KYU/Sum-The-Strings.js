/*
 ======= Problem Name: 
Sum The Strings

 ======= Problem description:
Create a function that takes 2 nonnegative integers in form of a string as an input, and outputs the sum (also as a string):

 ======= Solution:
*/
function sumStr(a,b) {
    const A = Number(a)
    const B = Number(b)
    return (A + B).toString()
    }